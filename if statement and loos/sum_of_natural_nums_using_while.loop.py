n=float(input('enter the number'))#n num of sum

sum=0           #initializing the sum
i=1             #setting the counter value
while i<=n:
    sum=sum+i   #adding the sum to counter value at initial state it is zero by incremenating the counter the sum will increase with respective to i
    i=i+1       # updating the counter
print('the sum of n is>>>>',sum)   # printing the sum out of the loop
